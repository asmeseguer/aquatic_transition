library(RevGadgets)

tree <- read.tree("data/GBOTB-genuslevel-ult-cut-angiosp-terrestrial.tre")

rev_out <- rev.process.div.rates(speciation_times_file = "output/EBD-terrestrial-10shifts/EBD_uniform_speciation_times.log",
                                 speciation_rates_file = "output/EBD-terrestrial-10shifts/EBD_uniform_speciation_rates.log",
                                 extinction_times_file = "output/EBD-terrestrial-10shifts/EBD_uniform_extinction_times.log",
                                 extinction_rates_file = "output/EBD-terrestrial-10shifts/EBD_uniform_extinction_rates.log",
                                 tree=tree,
                                 burnin=0.25,numIntervals=1000)

pdf("EBD-terrestrial-10shifts.pdf")
par(mfrow=c(2,2))
rev.plot.div.rates(rev_out,use.geoscale=FALSE)
dev.off()
